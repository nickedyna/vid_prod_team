def detect_production_mode(job: dict) -> tuple[str, Path | None]:
    talking_head = Path(job.get("talkingHeadAsset", "assets/talking_head/nat_intro_01.mp4"))
    if talking_head.exists() and not _is_placeholder(talking_head):
        return "hybrid", talking_head
    declared = job.get("productionMode")
    if declared:
        return declared, None
    return "faceless", None


def collect_missing_assets(job: dict) -> list[dict]:
    missing = []
    seen: set[str] = set()

    def add(entry: dict) -> None:
        if entry["path"] not in seen:
            seen.add(entry["path"])
            missing.append(entry)

    for item in job.get("videoPlan", []):
        clip_path = Path("assets") / item["clip"]
        if not clip_path.exists() or clip_path.stat().st_size == 0:
            status = "missing"
        else:
            # Placeholders are generated by this repo; flag them so Nat knows what to replace.
            status = "placeholder" if _is_placeholder(clip_path) else "real"
        if status != "real":
            default_hint = CHAPTER_CARD_HINT if "chapter_cards/" in item["clip"] else "Record or export this clip."
            add({
                "path": str(clip_path),
                "status": status,
                "howToCreate": REAL_ASSET_HINTS.get(item["clip"], default_hint),
            })

    for th in job.get("talkingHead", {}).get("clips", []):
        th_path = Path(th)
        if not th_path.exists() or _is_placeholder(th_path):
            add({
                "path": str(th_path),
                "status": "placeholder" if th_path.exists() else "missing",
                "howToCreate": "Film a talking-head clip: chest-up, dark backdrop, key light on face, direct eye contact.",
            })

    voiceover = Path(job.get("voiceover", ""))
    if job.get("voiceover") and not voiceover.exists():
        add({
            "path": str(voiceover),
            "status": "missing",
            "howToCreate": "Record the script as a WAV (quiet room, phone or USB mic is fine).",
        })

    music_dir = Path("assets/music")
    has_music = music_dir.exists() and (any(music_dir.glob("*.mp3")) or any(music_dir.glob("*.wav")))
    if not has_music:
        add({
            "path": "assets/music/corporate_tech_pulse_01.mp3",
            "status": "missing",
            "howToCreate": "Download a royalty-free clean corporate tech track (YouTube Audio Library works). Optional: video renders without it.",
        })
    return missing


def _is_placeholder(clip_path: Path) -> bool:
    marker = clip_path.with_suffix(".placeholder")
    return marker.exists()


def capcut_edit_plan(job: dict, mode: str, platform: str) -> str:
    lines = [
        f"# CapCut Edit Plan — {job.get('jobName', 'job')}",
        "",
        f"Production mode: **{mode}**",
        f"Platform preset: **{platform}** (1080x1920, 9:16, MP4, 30fps, AAC)" if platform == "shorts" else f"Platform preset: **{platform}**",
        "",
        "## Timeline order",
        "",
    ]
    t = 0.0
    for i, item in enumerate(job["videoPlan"], start=1):
        end = t + item["duration"]
        lines.append(
            f"{i}. `{item['clip']}` — {t:.0f}s to {end:.0f}s — overlay: \"{item['overlayText']}\" — motion: {item.get('motion', 'none')} — transition: {item.get('transition', 'cut')}"
        )
        t = end
    lines += [
        "",
        f"Total draft runtime: ~{t:.0f}s",
        "",
        "## Caption styling (AI Email Dev brand)",
        "",
        "- Bold white text (#F5F8FF), bottom safe zone, semi-transparent navy background.",
        "- Amber highlight (#FFB950) on proof phrases: \"overlooked path\", \"Brief → AI Agent → HTML → QA → ZIP\", \"proof-of-work\".",
        "- Import `04_captions.srt` for editable captions, or use CapCut auto-captions from the voiceover and correct against the SRT.",
        "",
        "## Voice + music",
        "",
        "- Voiceover at 100%. Music bed at 10% to 18% under voice.",
        "- Enable music ducking under voice if using CapCut auto-ducking.",
        "- If no licensed music track is available, ship voice-only. Voice is primary.",
        "",
        "## Punch-in moments",
        "",
        "- Punch in ~110% on \"AI-powered email development\" (0:07).",
        "- Punch in on the ZIP package reveal (\"Proof-of-Work Package\").",
        "- Hold steady on the flow map (Learn AI. Build proof. Grow your value.).",
        "",
        "## Finishing steps",
        "",
        "1. New CapCut project, import the whole `capcut_import/` folder.",
        "2. Drop `01_draft_short.mp4` on the timeline as the base.",
        "3. Replace placeholder segments with real screen recordings as they become available (same order/durations).",
        "4. Add voiceover track, align caption timings to voice.",
        "5. Apply caption style above, add subscribe end-card emphasis on the last 5 seconds.",
        "6. Export: 1080x1920, 9:16, MP4, 30fps, high bitrate, AAC audio.",
        "",
        "Do not automate publishing. Human review before upload.",
    ]
    if mode == "hybrid":
        lines += [
            "",
            "## Hybrid mode",
            "",
            "- Overlay `assets/talking_head/nat_intro_01.mp4` as picture-in-picture (lower-right) during the intro (0:00–0:11) and the CTA (final 8s).",
            "- Use the `talking_head_tech_frame` overlay if available.",
        ]
    return "\n".join(lines)


def production_notes(job: dict, mode: str, missing: list[dict], voiceover_used: bool, music_used: bool) -> str:
    lines = [
        f"# Production Notes — {job.get('jobName', 'job')}",
        "",
        f"- Brand: {job.get('brand', 'AI Email Dev')}",
        f"- Slogan: {job.get('slogan', 'Learn AI. Build proof. Grow your value.')}",
        f"- Production mode: **{mode}** " + ("(talking-head asset found)" if mode == "hybrid" else "(no talking-head asset found; faceless first)"),
        f"- Visual style: {job.get('visualStyle', 'brief_to_zip_flow_map')} (fallback: {job.get('fallbackVisualStyle', 'enterprise_grid_pulse')})",
        f"- Voiceover in draft render: {'yes' if voiceover_used else 'NO — silent draft, record voiceover next'}",
        f"- Music in draft render: {'yes' if music_used else 'no (optional; voice is primary)'}",
        "",
        "## Compliance",
        "",
        "- No income guarantees, no get-rich-quick claims. Practical, motivational, technical tone.",
        "",
        "## Missing / placeholder assets",
        "",
    ]
    if missing:
        for m in missing:
            lines.append(f"- `{m['path']}` — {m['status']}. {m['howToCreate']}")
    else:
        lines.append("- None. All assets are real.")
    lines += [
        "",
        "## Next actions",
        "",
        "1. Record the voiceover (short script) → save as `assets/voiceover/intro-video_01_voiceover.wav`.",
        "2. Record/export the screen-recording b-roll listed above (highest impact: html_email_code, chatgpt_agent_prompt, zip_package_folder).",
        "3. Re-run the full pipeline with `--mode full --voiceover assets/voiceover/intro-video_01_voiceover.wav`.",
        "4. Finish in CapCut using `capcut_edit_plan.md`, then human review before publish.",
    ]
    return "\n".join(lines)


