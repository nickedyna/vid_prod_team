def main() -> None:
    parser = argparse.ArgumentParser(description="Build the CapCut-ready review package for a job.")
    parser.add_argument("--job", required=True)
    parser.add_argument("--platform", default="shorts")
    parser.add_argument("--voiceover", default=None)
    parser.add_argument("--music", default=None)
    args = parser.parse_args()

    job_path = Path(args.job)
    job = json.loads(job_path.read_text(encoding="utf-8"))
    stem = job_path.stem

    review_dir = Path("output/review") / stem
    import_dir = review_dir / "capcut_import"
    review_dir.mkdir(parents=True, exist_ok=True)
    import_dir.mkdir(parents=True, exist_ok=True)

    mode, talking_head = detect_production_mode(job)
    missing = collect_missing_assets(job)

    # Core outputs from earlier pipeline stages
    short_src = Path("output/shorts") / f"{stem}.mp4"
    thumb_src = Path("output/thumbnails") / f"{stem}.jpg"
    srt_src = Path("output/captions") / f"{stem}.srt"

    if short_src.exists():
        shutil.copy2(short_src, review_dir / "short.mp4")
    if thumb_src.exists():
        shutil.copy2(thumb_src, review_dir / "thumbnail.jpg")
    if srt_src.exists():
        shutil.copy2(srt_src, review_dir / "captions.srt")

    (review_dir / "metadata.json").write_text(
        json.dumps(job["metadata"], indent=2, ensure_ascii=False) + "\n", encoding="utf-8"
    )

    voiceover_path = Path(args.voiceover) if args.voiceover else Path(job.get("voiceover", ""))
    voiceover_used = bool(args.voiceover) and Path(args.voiceover).exists()
    music_path = Path(args.music) if args.music else None
    music_used = bool(music_path and music_path.exists())

    (review_dir / "production_notes.md").write_text(
        production_notes(job, mode, missing, voiceover_used, music_used) + "\n", encoding="utf-8"
    )
    missing_lines = [f"# Missing / Placeholder Assets — {stem}", ""]
    if missing:
        for m in missing:
            missing_lines.append(f"- `{m['path']}` — **{m['status']}**. {m['howToCreate']}")
    else:
        missing_lines.append("- None. All assets are real.")
    (review_dir / "missing_assets.md").write_text("\n".join(missing_lines) + "\n", encoding="utf-8")

    # Preserve a hand-authored edit plan if one exists; otherwise generate the standard one.
    edit_plan_path = review_dir / "capcut_edit_plan.md"
    if not edit_plan_path.exists():
        edit_plan_path.write_text(capcut_edit_plan(job, mode, args.platform) + "\n", encoding="utf-8")
    if args.platform == "shorts":
        (review_dir / "longform_expansion.md").write_text(longform_expansion(job) + "\n", encoding="utf-8")

    # CapCut import folder
    import_files = []
    if short_src.exists():
        shutil.copy2(short_src, import_dir / "01_draft_short.mp4")
        import_files.append({"file": "01_draft_short.mp4", "role": "base timeline video", "status": "ready"})
    if voiceover_path and voiceover_path.exists():
        dest = import_dir / f"02_voiceover{voiceover_path.suffix}"
        shutil.copy2(voiceover_path, dest)
        import_files.append({"file": dest.name, "role": "primary voiceover", "status": "ready"})
    else:
        import_files.append({"file": "02_voiceover.wav", "role": "primary voiceover", "status": "missing — record and re-run full mode"})
    if music_used:
        dest = import_dir / f"03_music{music_path.suffix}"
        shutil.copy2(music_path, dest)
        import_files.append({"file": dest.name, "role": "music bed at 10-18%", "status": "ready"})
    else:
        import_files.append({"file": "03_music.mp3", "role": "music bed at 10-18%", "status": "optional — add royalty-free corporate tech track"})
    if srt_src.exists():
        shutil.copy2(srt_src, import_dir / "04_captions.srt")
        import_files.append({"file": "04_captions.srt", "role": "editable captions", "status": "ready"})
    if thumb_src.exists():
        shutil.copy2(thumb_src, import_dir / "05_thumbnail.jpg")
        import_files.append({"file": "05_thumbnail.jpg", "role": "thumbnail reference", "status": "ready"})

    style_asset = Path("assets/motion/backgrounds") / f"{job.get('visualStyle', 'brief_to_zip_flow_map')}.mp4"
    if style_asset.exists():
        shutil.copy2(style_asset, import_dir / "06_motion_background.mp4")
        import_files.append({"file": "06_motion_background.mp4", "role": "motion background", "status": "ready"})
    if talking_head:
        shutil.copy2(talking_head, import_dir / "07_talking_head.mp4")
        import_files.append({"file": "07_talking_head.mp4", "role": "talking-head PiP overlay", "status": "ready"})

    manifest = {
        "job": stem,
        "platform": args.platform,
        "productionMode": mode,
        "exportPreset": EXPORT_PRESETS.get(args.platform, EXPORT_PRESETS["shorts"]),
        "importFiles": import_files,
        "missingAssets": missing,
        "editPlan": "capcut_edit_plan.md",
        "notes": "Manual CapCut finishing only. No automated publishing or upload.",
    }
    (review_dir / "capcut_import_manifest.json").write_text(
        json.dumps(manifest, indent=2, ensure_ascii=False) + "\n", encoding="utf-8"
    )

    print(f"✅ Review package written: {review_dir}")
    if missing:
        print(f"⚠️ {len(missing)} missing/placeholder assets — see production_notes.md")


if __name__ == "__main__":
    main()
